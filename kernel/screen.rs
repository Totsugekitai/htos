use core::fmt::Write;

use crate::graphics::{Color, FrameBuffer};
use htlib::boot::BootInfo;
use htlib::mutex::Mutex;

pub enum WriteError {
    LineFeed,
    UnknownChar,
}

const SCREEN_WIDTH: usize = 80;
const SCREEN_HEIGHT: usize = 24 + 1;

pub struct Writer {
    position: (usize, usize),
    cb: [[u8; SCREEN_WIDTH]; SCREEN_HEIGHT],
    pub fb: FrameBuffer,
}

unsafe impl Send for Writer {}
unsafe impl Sync for Writer {}

impl Writer {
    pub const fn new() -> Self {
        Self {
            position: (0, 0),
            cb: [[0; SCREEN_WIDTH]; SCREEN_HEIGHT],
            fb: FrameBuffer::new(),
        }
    }

    fn write_byte(&mut self, c: u8, color: Color) -> Result<(), WriteError> {
        match c {
            b'\n' => {
                self.newline();
                Err(WriteError::LineFeed)
            }
            c => {
                if c > 0x7f {
                    Err(WriteError::UnknownChar)
                } else {
                    if self.position.0 >= SCREEN_WIDTH {
                        self.newline();
                    }
                    self.cb[self.position.1][self.position.0] = c;
                    self.write_ascii(c, color);
                    self.position.0 += 1;
                    Ok(())
                }
            }
        }
    }

    fn write_string(&mut self, s: &str, color: Color) -> Result<(), WriteError> {
        for byte in s.bytes() {
            match self.write_byte(byte, color) {
                Ok(_) => (),
                Err(e) => match e {
                    WriteError::LineFeed => (),
                    WriteError::UnknownChar => return Err(WriteError::UnknownChar),
                },
            }
        }
        Ok(())
    }

    fn newline(&mut self) {
        self.position.0 = 0;
        self.position.1 += 1;
        if self.position.1 >= SCREEN_HEIGHT {
            self.position.1 = SCREEN_HEIGHT - 1;
            self.screen_reload();
        }
    }

    fn screen_reload(&mut self) {
        for i in 1..SCREEN_HEIGHT {
            self.cb[i - 1] = self.cb[i];
        }
        self.cb[SCREEN_HEIGHT - 1] = [0; SCREEN_WIDTH];
        for y in 0..SCREEN_HEIGHT {
            for x in 0..SCREEN_WIDTH {
                let _ = self.write_ascii_with_position(self.cb[y][x], x, y, Color::White);
            }
        }
    }

    fn write_ascii(&mut self, c: u8, color: Color) {
        let font_data = get_font(c);
        let mut column = 0;
        for line in font_data {
            for row in 0..8 {
                let dot = (line << row) & 0b10000000;
                let xpos = self.position.0 * 8 + row;
                let ypos = self.position.1 * 16 + column;
                if dot == 0b10000000 {
                    self.fb.write(xpos, ypos, color);
                } else {
                    self.fb.write(xpos, ypos, Color::Black);
                }
            }
            column += 1;
        }
    }

    fn write_ascii_with_position(&mut self, c: u8, x: usize, y: usize, color: Color) {
        let font_data = get_font(c);
        let mut column = 0;
        for line in font_data {
            for row in 0..8 {
                let dot = (line << row) & 0b10000000;
                let xpos = x * 8 + row;
                let ypos = y * 16 + column;
                if dot == 0b10000000 {
                    self.fb.write(xpos, ypos, color);
                } else {
                    self.fb.write(xpos, ypos, Color::Black);
                }
            }
            column += 1;
        }
    }
}

impl Write for Writer {
    fn write_str(&mut self, s: &str) -> core::fmt::Result {
        match self.write_string(s, Color::White) {
            Ok(_) => Ok(()),
            Err(e) => match e {
                WriteError::LineFeed => Ok(()),
                WriteError::UnknownChar => Err(core::fmt::Error {}),
            },
        }
    }
}

pub static WRITER: Mutex<Writer> = Mutex::new(Writer::new());

pub fn init_writer(boot_info: &BootInfo) {
    let mut writer = WRITER.lock();
    writer.fb = FrameBuffer::from_boot_info(boot_info);
    writer.fb.write_background(Color::Black);
}

#[macro_export]
macro_rules! print {
    ($($arg:tt)*) => ($crate::screen::_print(format_args!($($arg)*)));
}

#[macro_export]
macro_rules! println {
    () => ($crate::print!("\n"));
    ($($arg:tt)*) => ($crate::print!("{}\n", format_args!($($arg)*)));
}

pub fn _print(args: core::fmt::Arguments) {
    use x86_64::instructions::interrupts;
    interrupts::without_interrupts(|| {
        WRITER.lock().write_fmt(args).unwrap();
    });
}

